// Function to show the paper
function showPaper() {
    document.getElementById('popupPaper').style.display = 'block';
    document.getElementById('overlay').style.display = 'block';
}

// Function to close the paper
function closePaper() {
    document.getElementById('popupPaper').style.display = 'none';
    document.getElementById('overlay').style.display = 'none';
}
